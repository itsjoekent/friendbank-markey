export default function getCopy(path, defaultToEnglish = true, fallback = '') {
  const language = isSpanishPath(location.pathname) ? SPANISH : ENGLISH;

  const all = { ...copy, ...window.__CAMPAIGN_COPY };
  const value = all[path];

  if (path.startsWith('config.')) {
    return value;
  }

  if (typeof value === 'undefined') {
    return fallback;
  }

  return value[language] || (defaultToEnglish && value[ENGLISH]) || fallback;
}
